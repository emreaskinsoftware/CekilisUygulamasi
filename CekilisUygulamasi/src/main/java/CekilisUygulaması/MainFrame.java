    private void btnCekilisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCekilisActionPerformed
         Random random = new Random();
        DefaultTableModel model = (DefaultTableModel) tblKisiler.getModel();
        int rowCount = model.getRowCount();
        Set<Integer> kazananIndeksler = new HashSet<>();
        StringBuilder kazananlar = new StringBuilder();

        while (kazananIndeksler.size() < 3 && kazananIndeksler.size() < rowCount) {
            int tercih = random.nextInt(rowCount);
            if (!kazananIndeksler.contains(tercih)) {
                kazananIndeksler.add(tercih);
                String kazanan = (String) model.getValueAt(tercih, 1);
                kazananlar.append(kazanan).append(", ");
            }
        }
        
        lblKazananlar.setText(kazananlar.toString());
        lblGösterge.setVisible(true);
        lblKazananlar.setVisible(true);
    }//GEN-LAST:event_btnCekilisActionPerformed

