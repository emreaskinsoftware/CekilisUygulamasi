    public ArrayList<String> getKisiler() throws FileNotFoundException {
        String yol = "C:/Users/Emre/Documents/NetBeansProjects/CekilisUygulamasi/src/main/java/CekilisUygulaması/Kisiler.txt";
        File file = new File(yol);
        Scanner scanner = new Scanner(file);
        ArrayList<String> kisiler = new ArrayList<>(); // ArrayList başlatıldı
        try {
            while (scanner.hasNextLine()) {
                String kisi = scanner.nextLine();
                kisiler.add(kisi);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return kisiler;
    }

